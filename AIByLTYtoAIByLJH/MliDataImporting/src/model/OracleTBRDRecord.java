package model;

public class OracleTBRDRecord {
	private String recordId;
	private String title;
	private String openDivCd;
	private String openGrade;
	private String partOpenRsn;
	private String openLimitPart;
	private String orgCd;
	private String orgNm;
	
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getOpenDivCd() {
		return openDivCd;
	}
	public void setOpenDivCd(String openDivCd) {
		this.openDivCd = openDivCd;
	}
	public String getOpenGrade() {
		return openGrade;
	}
	public void setOpenGrade(String openGrade) {
		this.openGrade = openGrade;
	}
	public String getPartOpenRsn() {
		return partOpenRsn;
	}
	public void setPartOpenRsn(String partOpenRsn) {
		this.partOpenRsn = partOpenRsn;
	}
	public String getOpenLimitPart() {
		return openLimitPart;
	}
	public void setOpenLimitPart(String openLimitPart) {
		this.openLimitPart = openLimitPart;
	}
	public String getOrgCd() {
		return orgCd;
	}
	public void setOrgCd(String orgCd) {
		this.orgCd = orgCd;
	}
	public String getOrgNm() {
		return orgNm;
	}
	public void setOrgNm(String orgNm) {
		this.orgNm = orgNm;
	}
}